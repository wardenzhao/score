public class HomeworkMsg {

    private String homeworkTime;
    private String homeworkContent;
    private String homeworkFinish;

    public String getHomeworkTime() {
        return homeworkTime;
    }

    public void setHomeworkTime(String homeworkTime) {
        this.homeworkTime = homeworkTime;
    }

    public String getHomeworkContent() {
        return homeworkContent;
    }

    public void setHomeworkContent(String homeworkContent) {
        this.homeworkContent = homeworkContent;
    }

    public String getHomeworkFinish() {
        return homeworkFinish;
    }

    public void setHomeworkFinish(String homeworkFinish) {
        this.homeworkFinish = homeworkFinish;
    }
}
